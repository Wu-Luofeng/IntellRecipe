SELECT '=== 商品列表 ===' AS info;
SELECT p.id, p.merchant_id, m.name AS merchant_name, p.name, p.price 
FROM product p JOIN merchant m ON p.merchant_id = m.id 
ORDER BY p.merchant_id, p.id;

SELECT '=== 商品-食材关联 ===' AS info;
SELECT pi.product_id, p.name AS product_name, pi.ingredient_id, i.name AS ingredient_name
FROM product_ingredient pi 
JOIN product p ON pi.product_id = p.id
JOIN ingredient i ON pi.ingredient_id = i.id
ORDER BY pi.product_id, pi.ingredient_id;

SELECT '=== 数据统计 ===' AS info;
SELECT 
  (SELECT COUNT(*) FROM merchant) AS merchant_count,
  (SELECT COUNT(*) FROM ingredient) AS ingredient_count,
  (SELECT COUNT(*) FROM product) AS product_count,
  (SELECT COUNT(*) FROM product_ingredient) AS product_ingredient_count;